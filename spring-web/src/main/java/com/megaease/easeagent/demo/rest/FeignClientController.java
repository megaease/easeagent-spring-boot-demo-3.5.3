package com.megaease.easeagent.demo.rest;

import com.megaease.easeagent.demo.api.HelloClient;
import jakarta.annotation.Resource;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RestController;

import java.io.IOException;

@RestController
public class FeignClientController {
    private static final Logger LOGGER = LoggerFactory.getLogger(FeignClientController.class);

    @Resource
    HelloClient helloClient;

    @GetMapping("/feignclient")
    public String client() throws IOException {
        LOGGER.info("call feignclient");
        return helloClient.hello();
    }
}
